type violationReportRow struct {
	ID          string    `json:"id"`
	ReporterID  string    `json:"reporter_id"`
	TargetID    string    `json:"target_id"`
	Reason      string    `json:"reason"`
	Detail      string    `json:"detail"`
	AutoGenerated bool    `json:"is_auto_generated"`
	CreatedAt   time.Time `json:"created_at"`
}

// AdminListViolationReports handles GET /admin/violation-reports.
// Query params: target_id, after (cursor by id).
func (h *Handler) AdminListViolationReports(w http.ResponseWriter, r *http.Request) {
	targetFilter := r.URL.Query().Get("target_id")
	after := r.URL.Query().Get("after")
	limit := 50

	rows, err := h.DB.Query(r.Context(),
		`SELECT id, reporter_id, target_id, reason, detail, is_auto_generated, created_at
		 FROM user_violation_reports
		 WHERE ($1 = '' OR target_id = $1::UUID)
		   AND ($2 = '' OR created_at < (SELECT created_at FROM user_violation_reports WHERE id = $2::UUID))
		 ORDER BY created_at DESC
		 LIMIT $3`,
		targetFilter, after, limit+1,
	)
	if err != nil {
		h.Logger.Error("admin list violation reports", "error", err)
		response.InternalError(w, r, h.Cfg.IsProduction())
		return
	}
	defer rows.Close()

	var reports []violationReportRow
	for rows.Next() {
		var rr violationReportRow
		if err := rows.Scan(&rr.ID, &rr.ReporterID, &rr.TargetID,
			&rr.Reason, &rr.Detail, &rr.AutoGenerated, &rr.CreatedAt); err != nil {
			continue
		}
		reports = append(reports, rr)
	}

	var cursor response.Cursor
	if len(reports) > limit {
		reports = reports[:limit]
		cursor.Next = reports[len(reports)-1].ID
	}
	response.JSONCursor(w, http.StatusOK, reports, cursor)
}

