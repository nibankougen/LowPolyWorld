type adminWorldRow struct {
	ID        string    `json:"id"`
	Name      string    `json:"name"`
	OwnerID   string    `json:"owner_user_id"`
	IsPublic  bool      `json:"is_public"`
	CreatedAt time.Time `json:"created_at"`
}

// AdminListWorlds handles GET /admin/worlds.
// Query params: q (name search), is_public (true/false), after.
func (h *Handler) AdminListWorlds(w http.ResponseWriter, r *http.Request) {
	q := r.URL.Query().Get("q")
	isPublicStr := r.URL.Query().Get("is_public")
	after := r.URL.Query().Get("after")
	limit := 50

	var isPublicFilter *bool
	if isPublicStr == "true" {
		v := true
		isPublicFilter = &v
	} else if isPublicStr == "false" {
		v := false
		isPublicFilter = &v
	}

	rows, err := h.DB.Query(r.Context(),
		`SELECT id, name, owner_user_id, is_public, created_at
		 FROM worlds
		 WHERE ($1 = '' OR name ILIKE '%'||$1||'%')
		   AND ($2::BOOLEAN IS NULL OR is_public = $2)
		   AND ($3 = '' OR created_at < (SELECT created_at FROM worlds WHERE id = $3::UUID))
		 ORDER BY created_at DESC
		 LIMIT $4`,
		q, isPublicFilter, after, limit+1,
	)
	if err != nil {
		h.Logger.Error("admin list worlds", "error", err)
		response.InternalError(w, r, h.Cfg.IsProduction())
		return
	}
	defer rows.Close()

	var worlds []adminWorldRow
	for rows.Next() {
		var w adminWorldRow
		if err := rows.Scan(&w.ID, &w.Name, &w.OwnerID, &w.IsPublic, &w.CreatedAt); err != nil {
			continue
		}
		worlds = append(worlds, w)
	}

	var cursor response.Cursor
	if len(worlds) > limit {
		worlds = worlds[:limit]
		cursor.Next = worlds[len(worlds)-1].ID
	}
	response.JSONCursor(w, http.StatusOK, worlds, cursor)
}

// AdminPatchWorld handles PATCH /admin/worlds/{worldID}.
// Body: { "is_public": false } — enables/disables a world.
func (h *Handler) AdminPatchWorld(w http.ResponseWriter, r *http.Request) {
	admin := middleware.AdminUserFromContext(r.Context())
	worldID := chi.URLParam(r, "worldID")

	var body struct {
		IsPublic *bool `json:"is_public"`
	}
	if err := json.NewDecoder(r.Body).Decode(&body); err != nil || body.IsPublic == nil {
		response.Error(w, r, http.StatusBadRequest, "validation_error", "is_public required")
		return
	}

	tag, err := h.DB.Exec(r.Context(),
		`UPDATE worlds SET is_public = $1, updated_at = now() WHERE id = $2`,
		*body.IsPublic, worldID,
	)
	if err != nil || tag.RowsAffected() == 0 {
		response.Error(w, r, http.StatusNotFound, "not_found", "world not found")
		return
	}

	action := "admin_disable_world"
	if *body.IsPublic {
		action = "admin_enable_world"
	}
	middleware.SetAdminAuditEntry(r.Context(), middleware.AdminAuditEntry{
		AdminID:    admin.ID,
		Action:     action,
		TargetType: "world",
		TargetID:   worldID,
	})
	w.WriteHeader(http.StatusNoContent)
}
